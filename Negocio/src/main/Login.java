    private void f_useridMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_f_useridMousePressed
       if (f_userid.getText().equals("ingrese la ID del usuario")){
        f_userid.setText("");
        f_userid.setForeground(Color.black);
       }
       if (String.valueOf(f_password.getPassword()).isEmpty()){
        f_password.setText("**********");
        f_password.setForeground(Color.gray);
       }
    }//GEN-LAST:event_f_useridMousePressed

    private void f_passwordMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_f_passwordMousePressed
        if(String.valueOf(f_password.getPassword()).equals("**********")){
            f_password.setText("");
            f_password.setForeground(Color.black);   
        }
        if(f_userid.getText().isEmpty()){
            f_userid.setText("ingrese la ID del usuario");
            f_userid.setForeground(Color.gray);
        }
    }//GEN-LAST:event_f_passwordMousePressed

    private void lb_ingresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lb_ingresarMouseClicked
        if (f_userid.getText().equals("Admin")){
            if(String.valueOf(f_password.getPassword()).equals("1234")){
                HomeAdmin newframe = new HomeAdmin();

                newframe.setVisible(true);

                this.dispose();
            }
        }else{
            HomeEmpleado newframe = new HomeEmpleado();

            newframe.setVisible(true);

            this.dispose();
        }
    }//GEN-LAST:event_lb_ingresarMouseClicked

